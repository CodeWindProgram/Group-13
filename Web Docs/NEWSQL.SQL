DROP TABLE STUDENTS;
DROP TABLE PROGRAMS;
DROP TABLE DEPARTMENTS;
DROP TABLE TEACHERS;
DROP TABLE PRINCIPAL;

CREATE TABLE PRINCIPAL (AID INT PRIMARY KEY , ANAME VARCHAR (50), APASS VARCHAR(50));
                   
CREATE TABLE TEACHERS (TID VARCHAR (50) PRIMARY KEY ,TPASS VARCHAR (50) ,TNAME VARCHAR (50), GENDER VARCHAR (10), POST VARCHAR (50), MAIL VARCHAR (50), PHONE INT);

CREATE TABLE DEPARTMENTS (DEPTID VARCHAR (50) PRIMARY KEY , DEPTNAME VARCHAR (50), DEPTFLR INT,HODID VARCHAR(50) ,  CONSTRAINT HODID FOREIGN KEY (HODID) REFERENCES TEACHERS (TID) );
                                                                                
CREATE TABLE PROGRAMS (PROGID VARCHAR (50) PRIMARY KEY, PROGNAME VARCHAR (50) , YEAR VARCHAR(50), SEM INT , DIV VARCHAR(50), DEPTID VARCHAR(50) , CONSTRAINT DEPTID FOREIGN KEY (DEPTID) REFERENCES DEPARTMENTS(DEPTID) , CAID VARCHAR(50)  , CONSTRAINT CAID FOREIGN KEY (CAID) REFERENCES TEACHERS(TID));

CREATE TABLE STUDENTS (STUDID VARCHAR (50) PRIMARY KEY, STUDNAME VARCHAR(50) , GENDER VARCHAR(10), YEAR VARCHAR(50), SEM INT, PROGID VARCHAR(50) , CONSTRAINT PROGID FOREIGN KEY (PROGID) REFERENCES PROGRAMS (PROGID) );

CREATE TABLE SUBJECTS (SUBID VARCHAR(50) PRIMARY KEY, SUBNAME VARCHAR(50) , PROGID VARCHAR(50) , CONSTRAINT PROGID FOREIGN KEY (PROGID) REFERENCES PROGRAMS (PROGID) , LECTID VARCHAR(50) , CONSTRAINT LECTID FOREIGN KEY (LECTID) REFERENCES TEACHERS (TID));

CREATE TABLE ATTENDANCE (ATTID INT PRIMARY KEY, STUDID VARCHAR(50) ,  ROOM INT, CLDATE DATE, STIME TIME (0) , ETIME TIME (0) , STATUS VARCHAR(10), SUBID VARCHAR(50) , CONSTRAINT SUBID FOREIGN KEY (SUBID) REFERENCES SUBJECTS(SUBID), LECTID VARCHAR(50), CONSTRAINT LECTID FOREIGN KEY (LECTID) REFERENCES TEACHERS (TID));

