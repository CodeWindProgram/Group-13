CREATE TABLE PRINCIPAL (AID INT PRIMARY KEY , ANAME VARCHAR (50), APASS VARCHAR(50));
                   
CREATE TABLE TEACHERS (TID VARCHAR (50) PRIMARY KEY ,TPASS VARCHAR (50) ,TNAME VARCHAR (50), GENDER VARCHAR (10), POST VARCHAR (50), MAIL VARCHAR (50), PHONE VARCHAR(50));

CREATE TABLE DEPARTMENTS (DEPTID VARCHAR (50) PRIMARY KEY , DEPTNAME VARCHAR (50), DEPTFLR INT,HODID VARCHAR(50) ,  CONSTRAINT HODID FOREIGN KEY (HODID) REFERENCES TEACHERS (TID) );
                                                                                
CREATE TABLE PROGRAMS (PROGID VARCHAR (50) PRIMARY KEY, PROGNAME VARCHAR (50) , YEAR VARCHAR(50), SEM INT , DIV VARCHAR(50), CAID VARCHAR(50), CONSTRAINT CAID FOREIGN KEY (CAID) REFERENCES TEACHERS(TID));

CREATE TABLE PARENTS (PID VARCHAR (50) PRIMARY KEY, NAME VARCHAR (50), PNO VARCHAR(50), OCCUPATION VARCHAR (50));

CREATE TABLE STUDENTS (STUDID VARCHAR (50) PRIMARY KEY, STUDNAME VARCHAR(50) , GENDER VARCHAR(10), YEAR VARCHAR(50), SEM INT, PROGID VARCHAR(50) , CONSTRAINT PROGID FOREIGN KEY (PROGID) REFERENCES PROGRAMS (PROGID) , PID VARCHAR (50), CONSTRAINT PID FOREIGN KEY (PID) REFERENCES PARENTS(PID) );

CREATE TABLE SUBJECTS (SUBID VARCHAR(50) PRIMARY KEY, SUBNAME VARCHAR(50) , PROGID VARCHAR(50) , CONSTRAINT PROGID FOREIGN KEY (PROGID) REFERENCES PROGRAMS (PROGID) , LECTID VARCHAR(50) , CONSTRAINT LECTID FOREIGN KEY (LECTID) REFERENCES TEACHERS (TID));

CREATE TABLE TTSEM7 (TTID INT PRIMARY KEY ,TIMING TIME (0) , DAY VARCHAR(50), SUBID VARCHAR(50), CONSTRAINT SUBID FOREIGN KEY (SUBID) REFERENCES SUBJECTS(SUBID));

CREATE TABLE TTSEM5 (TTID INT PRIMARY KEY,TIMING TIME (0) , DAY VARCHAR(50), SUBID VARCHAR(50), CONSTRAINT SUBID FOREIGN KEY (SUBID) REFERENCES SUBJECTS(SUBID));

CREATE TABLE TTSEM3 (TTID INT PRIMARY KEY,TIMING TIME (0) , DAY VARCHAR(50), SUBID VARCHAR(50), CONSTRAINT SUBID FOREIGN KEY (SUBID) REFERENCES SUBJECTS(SUBID));

CREATE TABLE ATTENDANCE_SEM7 (ATTID SERIAL PRIMARY KEY, STUDID VARCHAR(50) , CLDATE DATE , STATUS VARCHAR(10), TTID INT , CONSTRAINT TTID FOREIGN KEY (TTID) REFERENCES TTSEM7(TTID));

CREATE TABLE ATTENDANCE_SEM5 (ATTID SERIAL PRIMARY KEY, STUDID VARCHAR(50) , CLDATE DATE , STATUS VARCHAR(10), TTID INT , CONSTRAINT TTID FOREIGN KEY (TTID) REFERENCES TTSEM7(TTID));

CREATE TABLE ATTENDANCE_SEM3 (ATTID SERIAL PRIMARY KEY, STUDID VARCHAR(50) , CLDATE DATE , STATUS VARCHAR(10), TTID INT , CONSTRAINT TTID FOREIGN KEY (TTID) REFERENCES TTSEM7(TTID));

